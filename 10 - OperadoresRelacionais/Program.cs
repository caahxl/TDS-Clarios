int a = 10;
int b = 5;

//Maior
Console.WriteLine(a > b);

//Maior ou Igual
Console.WriteLine(a >= b);

//Menor
Console.WriteLine(a < b);

//Menor ou Igual
Console.WriteLine(a <= b);

//Igualdade
Console.WriteLine(a == b);

//Diferete
Console.WriteLine(a != b);

//Armazena valor booleano
bool boleano = a != b;
Console.WriteLine(boleano);